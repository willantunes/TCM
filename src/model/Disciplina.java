package model;

public class Disciplina {
	
	private int disciplina_id;
	private String nome;
	
	int getDisciplina_id() {
		return disciplina_id;
	}
	void setDisciplina_id(int disciplina_id) {
		this.disciplina_id = disciplina_id;
	}
	String getNome() {
		return nome;
	}
	void setNome(String nome) {
		this.nome = nome;
	}

}
