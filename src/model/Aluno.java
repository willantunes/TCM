package model;

public class Aluno {
	private int aluno_id;
	private int usuario_id;
	private int curso;
	
	int getAluno_id() {
		return aluno_id;
	}
	void setAluno_id(int aluno_id) {
		this.aluno_id = aluno_id;
	}
	int getUsuario_id() {
		return usuario_id;
	}
	void setUsuario_id(int usuario_id) {
		this.usuario_id = usuario_id;
	}
	int getCurso() {
		return curso;
	}
	void setCurso(int curso) {
		this.curso = curso;
	}
	

}
