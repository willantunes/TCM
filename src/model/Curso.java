package model;

public class Curso {
	
	private int curso_id;
	private String nome;
	
	int getCurso_id() {
		return curso_id;
	}
	void setCurso_id(int curso_id) {
		this.curso_id = curso_id;
	}
	String getNome() {
		return nome;
	}
	void setNome(String nome) {
		this.nome = nome;
	}

}
